# Values in Python
# This example demonstrates the different value types in Python.

# Strings are sequences of Unicode characters.
# They can be defined with single or double quotes.
print("Hello, World")
print("Hello with single quotes")

# Numbers in Python can be integers or floating-point.
print(42)  # Integer
print(3.14159)  # Float

# Booleans are represented by the values True and False.
print(True)
print(False)

# None is Python's null value.
print(None)

# Python has several built-in collections:
# Lists are ordered, mutable collections
my_list = [1, 2, 3, "four", 5.0]
print(my_list)

# Tuples are ordered, immutable collections
my_tuple = (1, 2, "three")
print(my_tuple)

# Dictionaries are key-value mappings
my_dict = {"name": "Python", "version": 3.9}
print(my_dict)
